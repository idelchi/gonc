package main

import (
	"fmt"
	"os"
	"runtime"
	"strings"

	"github.com/idelchi/gonc/internal/commands"
	"github.com/idelchi/gonc/internal/config"
	"github.com/spf13/cobra"
	"github.com/spf13/viper"
)

var version = "unknown - unofficial & generated by unknown"

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run() error {
	// Configure viper first
	viper.SetEnvPrefix("gonc")
	viper.SetEnvKeyReplacer(strings.NewReplacer(".", "_"))
	viper.AutomaticEnv()

	cfg := &config.Config{}

	root := &cobra.Command{
		Use:   "gonc",
		Short: "File encryption utility",
		Long: `A file encryption utility that supports deterministic and non-deterministic modes.
Provides commands for key generation, encryption, and decryption.`,
		Version:          version,
		TraverseChildren: true,
		Run: func(cmd *cobra.Command, args []string) {
			fmt.Println("Must provide a subcommand. Run 'gonc --help' for usage.")
		},
		PersistentPreRunE: func(cmd *cobra.Command, args []string) error {
			// Configure Viper to read from environment variables
			viper.SetEnvPrefix("gonc")
			viper.AutomaticEnv()

			// Bind all flags to viper with command prefix
			if err := viper.BindPFlags(cmd.Root().Flags()); err != nil {
				return fmt.Errorf("failed to bind flags: %w", err)
			}

			if err := viper.BindPFlags(cmd.Flags()); err != nil {
				return fmt.Errorf("failed to bind persistent flags: %w", err)
			}

			// // Unmarshal the config into our struct
			if err := viper.Unmarshal(cfg); err != nil {
				return fmt.Errorf("failed to unmarshal config: %w", err)
			}

			return nil
		},
	}

	root.CompletionOptions.DisableDefaultCmd = true

	// Add persistent flags - they'll be automatically bound by viper
	root.Flags().StringP("key", "k", "", "Encryption key (32 bytes, hex-encoded)")
	root.Flags().IntP("parallel", "j", runtime.NumCPU(), "Number of parallel workers")

	gen := commands.NewGenerateCmd()
	encrypt := commands.NewEncryptCmd(cfg)
	decrypt := commands.NewDecryptCmd(cfg)

	encrypt.Flags().StringP("suffix", "s", ".enc", "Suffix to append to encrypted files")
	decrypt.Flags().StringP("suffix", "s", "", "Suffix to append to decrypted files. If empty, the suffix will be removed")

	gen.SetHelpFunc(func(command *cobra.Command, strings []string) {
		// Hide flag for this command
		command.Flags().MarkHidden("key")
		command.Flags().MarkHidden("parallel")
		// Call parent help func
		command.Parent().HelpFunc()(command, strings)
	})

	// Add commands
	root.AddCommand(
		gen,
		encrypt,
		decrypt,
	)

	encrypt.Flags().BoolP("deterministic", "d", false, "Use deterministic encryption mode")

	root.CompletionOptions.HiddenDefaultCmd = true

	return root.Execute()
}

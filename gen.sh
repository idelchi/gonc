package main

import (
	"fmt"
	"os"
	"strings"

	"github.com/spf13/cobra"
	"github.com/spf13/viper"
	"github.com/idelchi/gonc/internal/commands"
	"github.com/idelchi/gonc/internal/config"
)

var version = "unknown - unofficial & generated by unknown"

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run() error {
	root := &cobra.Command{
		Use:   "gonc",
		Short: "File encryption utility",
		Long: `A file encryption utility that supports deterministic and non-deterministic modes.
Provides commands for key generation, encryption, and decryption.`,
	}

	// Configure viper
	viper.SetEnvPrefix("GONC")
	viper.SetEnvKeyReplacer(strings.NewReplacer(".", "_"))
	viper.AutomaticEnv()

	// Add persistent flags
	root.PersistentFlags().StringP("key", "k", "", "Encryption key (32 bytes, hex-encoded)")
	root.PersistentFlags().IntP("parallel", "j", 1, "Number of parallel workers")

	// Bind persistent flags to viper
	if err := viper.BindPFlags(root.PersistentFlags()); err != nil {
		return fmt.Errorf("binding persistent flags: %w", err)
	}

	// Add commands
	root.AddCommand(
		commands.NewGenerateCmd(),
		commands.NewEncryptCmd(),
		commands.NewDecryptCmd(),
	)

	return root.Execute()
}
EOF


package commands

import (
	"crypto/rand"
	"encoding/hex"
	"fmt"

	"github.com/spf13/cobra"
	"github.com/spf13/viper"
	"github.com/idelchi/gonc/internal/config"
	"github.com/idelchi/gonc/internal/encryption"
)

func NewGenerateCmd() *cobra.Command {
	return &cobra.Command{
		Use:     "generate",
		Aliases: []string{"gen"},
		Short:   "Generate a new encryption key",
		RunE: func(cmd *cobra.Command, args []string) error {
			key := make([]byte, 32)
			if _, err := rand.Read(key); err != nil {
				return fmt.Errorf("generating key: %w", err)
			}
			fmt.Println(hex.EncodeToString(key))
			return nil
		},
	}
}

func NewEncryptCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:     "encrypt [flags] files...",
		Aliases: []string{"enc"},
		Short:   "Encrypt files",
		RunE: func(cmd *cobra.Command, args []string) error {
			cfg := config.Config{
				Key:          viper.GetString("key"),
				Parallel:     viper.GetInt("parallel"),
				Deterministic: viper.GetBool("deterministic"),
				Files:        args,
			}

			if err := cfg.Validate(); err != nil {
				return err
			}

			return runProcessor(cfg)
		},
	}

	cmd.Flags().Bool("deterministic", false, "Use deterministic encryption mode")
	_ = viper.BindPFlag("deterministic", cmd.Flags().Lookup("deterministic"))

	return cmd
}

func NewDecryptCmd() *cobra.Command {
	return &cobra.Command{
		Use:     "decrypt [flags] files...",
		Aliases: []string{"dec"},
		Short:   "Decrypt files",
		RunE: func(cmd *cobra.Command, args []string) error {
			cfg := config.Config{
				Key:      viper.GetString("key"),
				Parallel: viper.GetInt("parallel"),
				Decrypt:  true,
				Files:    args,
			}

			if err := cfg.Validate(); err != nil {
				return err
			}

			return runProcessor(cfg)
		},
	}
}

func runProcessor(cfg config.Config) error {
	proc, err := encryption.NewProcessor(cfg)
	if err != nil {
		return fmt.Errorf("creating processor: %w", err)
	}

	return proc.ProcessFiles()
}
